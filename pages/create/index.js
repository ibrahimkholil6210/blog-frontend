import React from "react";
import Head from "next/head";
import * as Yup from "yup";
import { Formik, Form, Field } from "formik";
import axios from "../../lib/axios";
import { Button } from "..";
import styles from "../../styles/Create.module.css";

const CreatePost = () => {
  const validationSchemas = Yup.object().shape({
    title: Yup.string().required("Title is required"),
    content: Yup.string().required("Content is required"),
    date: Yup.date().required("Date is required"),
  });

  const handleSubmit = async (values) => {
    try {
      console.log(values);
      const apiRes = await axios.post("/posts", {
        title: values.title,
        content: values.content,
        date: values.date,
      });
      console.log(apiRes);
    } catch (err) {
      console.log(err.message);
    }
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create | Next Blog App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <div>
          <h1 className={styles.title}>Create a new post</h1>
          <div className={styles.inputContainer}>
            <Formik
              initialValues={{
                title: "",
                content: "",
                date: "",
              }}
              validationSchema={validationSchemas}
              onSubmit={handleSubmit}
            >
              {({ touched, errors }) => (
                <Form>
                  <div className={styles.inputRow}>
                    <div className={styles.label}>Title</div>
                    <Field name="title" />
                    {errors.title && touched.title ? (
                      <div className={styles.errorMessage}>{errors.title}</div>
                    ) : null}
                  </div>
                  <div className={styles.inputRow}>
                    <div className={styles.label}>Content</div>
                    <Field name="content" as="textarea" />
                    {errors.content && touched.content ? (
                      <div className={styles.errorMessage}>
                        {errors.content}
                      </div>
                    ) : null}
                  </div>
                  <div className={styles.inputRow}>
                    <div className={styles.label}>Date</div>
                    <Field name="date" type="date" />
                    {errors.date && touched.date ? (
                      <div className={styles.errorMessage}>{errors.date}</div>
                    ) : null}
                  </div>
                  <div className={styles.inputRow}>
                    <Button type="submit" label="Create" />
                  </div>
                </Form>
              )}
            </Formik>
          </div>
        </div>
      </main>
    </div>
  );
};

export default CreatePost;
